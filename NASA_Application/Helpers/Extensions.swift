import UIKit

extension UIColor {
    static func nasaRed() -> UIColor {
        return UIColor(red: 252/255, green: 61/255, blue: 33/255, alpha: 1)
    }
    
    static func nasaBlue() -> UIColor {
        return UIColor(red: 11/255, green: 61/255, blue: 145/255, alpha: 1)
    }
}
